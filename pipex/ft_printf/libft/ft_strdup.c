#include "libft.h"

char	*ft_strdup(const char *s1)
{
	unsigned char	*s2;
	size_t			i;

	s2 = malloc(sizeof(char) * (ft_strlen(s1)) + 1);
	if (!s2)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return ((char *)s2);
}
